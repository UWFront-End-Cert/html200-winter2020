const x = 10;
const y = 2;
const z = 5;
const a = 3;

console.log('x+y', x + y);
console.log('y-a', y - a);
console.log('z*y', z * y);
console.log('y/x', y / x);
console.log('y%y', y % y);
console.log('z**a', z ** a);
console.log('a++', a++); // a = a + 1;
console.log('z--', z--); // z = z - 1;
console.log('-x', -x);
console.log('x+z*(y-a)*a', x + z * (y - a) * a);
console.log('2+y*(4-z)/y', 2 + y * (4 - z) / y);
console.log('(y%y)+4-(a-y)', (y % y) + 4 - (a - y));